#[5.2.3]ハッシュの同値比較、要素数の取得、要素の削除
#「==」でハッシュ同士を比較すると、同じハッシュかどうかをチェックできます。この時すべてのキーと値が同じであればtrueが返ります。
# EX
 a = { 'x' => 1, 'y' => 2, 'z' => 3 }

 # すべてのキーと値が同じであればtrue
 b = { 'x' => 1, 'y' => 2, 'z' => 3 }
 puts a == b
 # ==> true

 #並び順が逆になっていてもキーと値がすべて同じならtrue
 c = { 'z' => 3, 'x' => 1, 'y' => 2 }
 puts a == c
 # ==> true

 #キー'x'の値が異なるのでfalse
 d = { 'x' => 10, 'y' => 2, 'z' => 3 }
 puts a == d
 # ==> false

#sizeメソッド(エイリアスメソッドはlength)を使うとハッシュの要素の個数を調べることができます。

  puts {}.size
  # ==> 0

  puts { 'x' => 1, 'y' => 2, 'z' => 3 }.size
  # ==> 3

#deleteメソッドを使うと指定したキーに対おする要素を削除できます。戻り値はされた要素の値です。

  currencies = {'japan' => 'yen', 'us' => 'dollar', 'india' => 'rupee'}
  currencies.delete('japan')
  puts currencies
  # ==> {"us"=>"dollar", "india"=>"rupee"}

#deleteメソッドで指定したキーがなければnilが返ります。ブロックを渡すと、キーが見つからないときにブロックの戻り値をdeleteメソッドの戻り値にできます。
  currencies = {'japan' => 'yen', 'us' => 'dollar', 'india' => 'rupee'}

  # 削除しようとしたキーが見つからない時はnilが返る
  puts currencies.delete('italy')
  # ==> nil

  # ブロックを渡すとキーが見つからない時の戻り値を作成できる
  puts currencies.delete('italy') { |key| "Not found: #{key}"}
  # ==> Not found: italy

